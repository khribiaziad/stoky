/**
 * Returns a translation function for the given language.
 * Usage: const t = useT(lang)  →  t('save') → 'Save' / 'Enregistrer' / 'حفظ'
 */
export function useT(lang = 'en') {
  const locale = T[lang] || T.en;
  return (key) => locale[key] ?? T.en[key] ?? key;
}

